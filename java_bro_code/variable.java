/*  Variable
 * it is a placeholder for a value that behaves as the value it contains
 */
public class variable {
    public static void main(String[]args){
        /*   int x;       declaration
                             +    
          x=10;         assignment
                             =
          int x = 10;   initialization
         */

     int x = 123;     
     long y = 6454984567465455646L;   // capital L is used in last then output will display                   
     byte z = 127;              // byte can only store up to 127   
     byte k = -128;             // it can store from -128 to 127
     
     System.out.println(x);    
     System.out.println("My number is:- "+x);
     System.out.println("My number is= "+y);
     System.out.println("My number is = "+z);  
     System.out.println("My number is= "+k);
    }
}
